import type { FungalZone } from "./types";

function generateRainHistory(baseMm: number): FungalZone["rainHistory"] {
  const days: FungalZone["rainHistory"] = [];
  for (let i = 13; i >= 0; i--) {
    const date = new Date();
    date.setDate(date.getDate() - i);
    const variation = Math.sin(i * 0.8) * 8 + Math.cos(i * 1.3) * 3;
    days.push({
      date: date.toISOString().split("T")[0],
      mm: Math.max(0, Math.round((baseMm + variation) * 10) / 10),
    });
  }
  return days;
}

function generateHourlyForecasts(
  baseTemp: number,
  baseHumidity: number,
  baseSoil: number
): FungalZone["hourlyForecasts"] {
  const hours: FungalZone["hourlyForecasts"] = [];
  for (let h = 0; h < 24; h++) {
    const tempCycle = Math.sin(((h - 6) * Math.PI) / 12) * 6;
    const humidityCycle = Math.cos(((h - 5) * Math.PI) / 12) * 15;
    hours.push({
      hour: h,
      temperature: Math.round((baseTemp + tempCycle) * 10) / 10,
      humidity: Math.min(100, Math.max(40, Math.round(baseHumidity + humidityCycle))),
      soilMoisture: Math.min(100, Math.max(20, Math.round((baseSoil + humidityCycle * 0.3) * 10) / 10)),
    });
  }
  return hours;
}

